#include "ft_printf.h"

int	ft_hexlen(int nbr)
{
	int	len;

	len = 0;
	if (nbr == 0)
	{
		nbr /= 16;
		len++;
	}
	while (nbr)
	{
		nbr /= 16;
		len++;
	}
	return (len);
}
